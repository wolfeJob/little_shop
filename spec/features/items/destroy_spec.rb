require 'rails_helper'

RSpec.describe 'item delete', type: :feature do
  describe 'when I visit an item show page' do
    before :each do
      @bike_shop = Merchant.create(name: "Brian's Bike Shop", address: '123 Bike Rd.', city: 'Richmond', state: 'VA', zip: 80203)
      @chain = @bike_shop.items.create(name: "Chain", description: "It'll never break!", price: 50, image: "https://www.rei.com/media/b61d1379-ec0e-4760-9247-57ef971af0ad?size=784x588", inventory: 5)
    end

    it 'I can delete an item' do
      visit "/items/#{@chain.id}"

      expect(page).to have_link("Delete Item")

      click_on "Delete Item"

      expect(current_path).to eq("/items")
      expect(page).to_not have_css("#item-#{@chain.id}")
    end

    it 'If an item is ordered then I get a message that it cannot be deleted when I try to delete' do
       order = Order.create(username: 'Christopher', address: '1234 5th St', city: 'Denver', state: 'CO', zipcode: 80021)
       OrderItem.create(order: order, item: @chain, quantity: 1, price: @chain.price)

       visit "/items/#{@chain.id}"

       click_on "Delete Item"

       expect(page).to have_content("#{@chain.name} cannot be deleted becasue this item is on order")
     end
  end
end
